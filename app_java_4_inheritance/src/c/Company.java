package c;

//Inheritance example

public class Company {
	public void work() {
		System.out.println("working...");
	}

	public void salary() {
		System.out.println("getting salary...");
	}

	public void attendance() {
		System.out.println("present...");
	}

	public void lunch() {
		System.out.println("having lunch...");
	}
}

package nested_if_else;

//nested if..else

public class C {
	public static void main(String[] args) {
		int x = 10;

		if (x > 20) {
			System.out.println(1);
		} else if (x > 30) {
			System.out.println(2);
		} else if (x > 40) {
			System.out.println(3);
		} else {
			System.out.println("else");
		}
	}
}
